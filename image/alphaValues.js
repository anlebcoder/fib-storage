var getAlphaValue = module.exports.getAlphaValue = function(r) {
	if (r <= 0) {
		return [];
	}

	var width, height, diameter;
	width = height = 1000;
	diameter = 2 * r;
	var img = gd.create(width, height);
	img.alphaBlending = false;
	img.filledRectangle(0, 0, width, height, 0x7fffffff);
	img.filledEllipse(width / 2, height / 2, width, height, 0xff0000);
	img = img.resample(diameter, diameter);

	var a,
		b,
		c = [],
		d = 0;

	function filterLen(r) {
		if (r < 5) return 0;
		var h = Math.sqrt(r * r * 2) - r;
		return Math.floor(Math.sqrt(h * h * 2)) - 1;
	}

	d = filterLen(r);

	for (var i = 0; i < r; i++) {
		b = [];
		for (var j = 0; j < r; j++) {
			a = img.getPixel(i, j) >> 24;
			if (!a) continue;
			b[j] = a;

			if (a === 127 && j) {
				for (var q = 0; q < b.length; q++) {
					b[q] = 127;
				}
			}
		}
		if (b.length) {
			if (d > 0) {
				b = b.slice(d);
				d = d - 1;
			}
			c[i] = b;
		}
	}
	return c;
}

module.exports.getAlphaValues = function(raduis) {
	raduis = raduis || MAX_RADUIS;

	var all = [];
	for (var r = 1; r <= raduis; r++) {
		all[r - 1] = getAlphaValue(r);
	}

	return all;
}